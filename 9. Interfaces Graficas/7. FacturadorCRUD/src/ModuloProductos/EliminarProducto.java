    int posicioneliminar;
    private void btnBuscarCedulaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCedulaActionPerformed
        String numeroCedula = campoBusCedula.getText();
        boolean usuarioEncontrado = false;
        for (int i = 0; i < this.ventanaMenu.listaClientes.length; i++) {
            posicioneliminar=i;
            if (this.ventanaMenu.listaClientes[i] != null && this.ventanaMenu.listaClientes[i].getCedula().equals(numeroCedula)) {
                etq_Buscando.setText("Usuario Encontrado");
                campoNombres.setText(this.ventanaMenu.listaClientes[i].getNombres());
                campoApellidos.setText(this.ventanaMenu.listaClientes[i].getApellidos());
                campoCedula.setText(this.ventanaMenu.listaClientes[i].getCedula());
                    
                campoNombres.setEnabled(false);
                campoApellidos.setEnabled(false);
                campoCedula.setEnabled(false);
                    //posicionModificar = i;
  
                usuarioEncontrado = true; // El usuario ha sido encontrado
                break; // Salir del ciclo una vez encontrado
            }
        }

        if (!usuarioEncontrado) {
            etq_Buscando.setText("Usuario No Encontrado");
            campoNombres.setText("");
            campoApellidos.setText("");
            campoCedula.setText("");
        }
    }//GEN-LAST:event_btnBuscarCedulaActionPerformed

