    private void btnEliminarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUsuarioActionPerformed
        this.ventanaMenu.listaClientes[posicioneliminar] = null;

        // Desplazar las siguientes posiciones hacia atrás
        for (int i = posicioneliminar + 1; i < this.ventanaMenu.listaClientes.length; i++) {
            this.ventanaMenu.listaClientes[i - 1] = this.ventanaMenu.listaClientes[i];
        }
        this.ventanaMenu.listaClientes[this.ventanaMenu.listaClientes.length - 1] = null;
        System.out.println("Usuario Eliminado");
        campoNombres.setText("");
        campoApellidos.setText("");
        campoCedula.setText("");
        campoNombres.setEnabled(true);
        campoApellidos.setEnabled(true);
        campoCedula.setEnabled(true);

        etq_Buscando.setText("Usuario Eliminado");
    }//GEN-LAST:event_btnEliminarUsuarioActionPerformed

  
